		<h2 class="newsletter-country-name"><a href="">Tanzania</a></h2>
		<h3 class="newsletter-news-date">(November 3, 2005)</h3>
		<p class="newsletter-text">Following the death of Jumbe Rajab Jumbe, running mate on the CHADEMA party's ticket, the mainland general elections initially slated for October 30 were postponed to December 18. This decision did not affect the semi-autonomous region of Zanzibar, where elections for the local president and parliament took place on October 30 as planned. Two days after the poll, Zanzibar's Electoral Commission announced incumbent Amani Abeid Karume as the winner of the presidential race with 53.2% of the vote; his main competitor, Seif Sharif Hamad of the Civic United Front (CUF) obtained 46.1%. In the race for the House of Representatives, the ruling Chama Cha Mapinduzi party (CCM) won 30 seats and CUF 19. The elections were marred by widespread allegations of voting irregularities and intimidation of opposition groups.</p>

		<h2 class="newsletter-country-name"><a href="">Iraq</a></h2>
		<h3 class="newsletter-news-date">(November 5, 2005)</h3>
		<p class="newsletter-text">The Electoral Commission announced on October 25 the results of the October 15 referendum to approve the country's constitution. The constitution was ratified with 78% of the votes: 7,742,796 people pronounced themselves in favor and 2,109,495 voted against the document. In order for the referendum to pass, the constitution had to be approved by a majority of the voters and not be rejected by two-thirds of the voters in three or more of Iraq's eighteen governorates. Three Sunni-dominated governorates voted against the constitution, but the share of the “no” votes was higher than two-thirds (67%) in only two of them, Anbar and Saladdeen. The approved constitution paves the way for elections for a permanent legislature on December 15, 2005. produced for other electoral events as well, such as noteworthy local or mayoral elections and referenda.</p>

